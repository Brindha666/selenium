package org.testleaf.week1.day2;

public class findoutodd {

    

    public static void main(String[] args) {

        for (int a = 1; a <= 10; a++) {

            if (a % 2 == 0) {
                System.out.println(a + " is an Even number");
            } else {
                System.out.println(a + " is an Odd number");
            }

        }
    }
}
